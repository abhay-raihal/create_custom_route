    public static function getStatesByCode($country): array
    {
        if (empty(Country::getCountryNameByCode($country))) {
            return [];
        }

        $data = array(
            self::COUNTRYNAME => Country::getCountryNameByCode($country),
            self::COUNTRYALPHA2CODE => $country,
            self::COUNTRYALPHA3CODE => Country::getCountryAlpha3Code($country),
            self::STATES => array()
        );

        $stateDetails = self::$countryStateMap[$country];

        foreach ($stateDetails as $state) {
            $data[self::STATES][] = array(
                self::STATENAME => self::$stateNameMappings[$state],
                self::STATECODE => $state,
            );
        }

        return $data;
    }
