    public static function exists($code)
    {
        return defined(get_class() . '::' . strtoupper($code));
    }

    public static function getCountryCode($value)
    {
        if (self::exists($value) === true)
        {
            return $value;
        }

        $countryCode = array_search($value, self::$countryCodeNameMap);

        if ($countryCode === false)
        {
            return null;
        }

        return $countryCode;
    }

    public static function getCountryNameByCode(string $code)
    {
        return self::$countryCodeNameMap[$code] ?? null;
    }

    public static function checkIfValidCountry($value)
    {
        if (self::getCountryCode($value) === null)
        {
            return false;
        }

        return true;
    }

    /* Obtain the Alpha-3 code by providing the Alpha-2 code of a country
    */
    public static function getCountryAlpha3Code(string $code)
    {
        return self::$countryAlpha3CodeMap[$code] ?? null;
    }

    /* Obtain the 2 country codes and compare
  */
    public static function matches(string $country1, string $country2)
    {
        return strcasecmp($country1, $country2) == 0;
    }

    public static function getcountryDetails()
    {
        $data = array();

        foreach (self::$countryCodeMapping as $country)
        {
            $countryDetails = array(
             self::COUNTRYNAME => Country::getCountryNameByCode($country),
             self::COUNTRYALPHA2CODE => $country,
             self::COUNTRYALPHA3CODE => Country::getCountryAlpha3Code($country),
            );

            array_push($data, $countryDetails);
        }

        return $data;
    }

    public static function getCountryDetailsFromCountryCode($country)
    {
            $countryDetails = array(
                self::COUNTRYNAME => self::getCountryNameByCode($country),
                self::COUNTRYALPHA2CODE => $country,
                self::COUNTRYALPHA3CODE => self::getCountryAlpha3Code($country),
            );
        return $countryDetails;
    }
